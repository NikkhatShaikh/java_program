package interviewPrograms;

public class SwapNumber {
    public static void main(String[] args) {

        int a= 10, b=20;
        System.out.println("Before Swapping");
        System.out.println("a= "+a+" b="+b);
        b=a;    // b= 10
        a=a+b; // 10+10 = 220

        System.out.println("after Swapping");
        System.out.println("a= "+a+" b="+b);




    }
}
